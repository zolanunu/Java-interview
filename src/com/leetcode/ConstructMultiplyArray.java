	public int[] multiply(int[] A) {
		if(A.length == 0 || A == null) {
			return A;
		}
		int n = A.length;
		int[] B = new int[n];
		for(int i = 0; i < n; i++) {
			int multiply = 1;
			for(int j = 0; j < n; j++) {
				if(i != j) {
					multiply = multiply * A[j];
				} else {
					continue;
				}
			}
			B[i] = multiply;
		}
		return B;
	}
	
	

